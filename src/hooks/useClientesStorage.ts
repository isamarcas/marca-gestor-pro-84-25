import { Cliente } from '@/types';

const STORAGE_KEY = 'clientes';

// Sistema de eventos para notificar mudan√ßas
class ClientesStorageManager {
  private listeners: Set<() => void> = new Set();

  subscribe(listener: () => void) {
    console.log('[ClientesStorageManager] üìù Registrando listener. Total:', this.listeners.size + 1);
    this.listeners.add(listener);
    return () => {
      console.log('[ClientesStorageManager] üóëÔ∏è Removendo listener. Total restante:', this.listeners.size - 1);
      this.listeners.delete(listener);
    };
  }

  notify() {
    console.log('[ClientesStorageManager] üîî Notificando', this.listeners.size, 'listeners');
    this.listeners.forEach(listener => {
      try {
        listener();
      } catch (error) {
        console.error('[ClientesStorageManager] ‚ùå Erro ao executar listener:', error);
      }
    });
  }
}

export const clientesStorageManager = new ClientesStorageManager();

// Fun√ß√£o centralizada para carregar clientes
export const forceLoadClientes = (): Cliente[] => {
  try {
    console.log('[forceLoadClientes] üîç Carregando clientes...');
    
    const stored = localStorage.getItem(STORAGE_KEY);
    console.log('[forceLoadClientes] üì¶ Dados brutos:', stored);
    
    if (!stored || stored === 'null' || stored === 'undefined' || stored === '[]') {
      console.log('[forceLoadClientes] ‚ö†Ô∏è Dados vazios, retornando array vazio');
      return [];
    }

    const parsed = JSON.parse(stored);
    
    if (!Array.isArray(parsed)) {
      console.warn('[forceLoadClientes] ‚ö†Ô∏è Dados n√£o s√£o array, reinicializando');
      localStorage.setItem(STORAGE_KEY, JSON.stringify([]));
      return [];
    }
    
    const clientes = parsed.map((c: any) => ({
      ...c,
      totalMarcas: c.totalMarcas || 0,
      marcasAtivas: c.marcasAtivas || 0,
      origem: c.origem || 'manual',
      createdAt: c.createdAt ? new Date(c.createdAt) : new Date(),
      updatedAt: c.updatedAt ? new Date(c.updatedAt) : new Date(),
    }));
    
    console.log('[forceLoadClientes] ‚úÖ Carregados:', clientes.length, 'clientes');
    console.log('[forceLoadClientes] üìã Lista:', clientes.map(c => ({ id: c.id, nome: c.nome, email: c.email, origem: c.origem })));
    
    return clientes;
  } catch (error) {
    console.error('[forceLoadClientes] üí• Erro:', error);
    localStorage.setItem(STORAGE_KEY, JSON.stringify([]));
    return [];
  }
};

// Fun√ß√£o ROBUSTA para salvar clientes
export const forceSaveClientes = (clientes: Cliente[]): boolean => {
  try {
    console.log('[forceSaveClientes] üíæ SALVANDO', clientes.length, 'clientes...');
    console.log('[forceSaveClientes] üìù Detalhes dos clientes:', clientes.map(c => ({ id: c.id, nome: c.nome, email: c.email, origem: c.origem })));
    
    // Preparar dados para salvamento
    const clientesParaSalvar = clientes.map(c => ({
      ...c,
      createdAt: c.createdAt instanceof Date ? c.createdAt.toISOString() : c.createdAt,
      updatedAt: c.updatedAt instanceof Date ? c.updatedAt.toISOString() : c.updatedAt,
    }));
    
    // Salvar diretamente
    const dadosString = JSON.stringify(clientesParaSalvar);
    localStorage.setItem(STORAGE_KEY, dadosString);
    
    // VERIFICA√á√ÉO CR√çTICA IMEDIATA
    const verificacao = localStorage.getItem(STORAGE_KEY);
    
    if (!verificacao) {
      console.error('[forceSaveClientes] ‚ùå Falha na verifica√ß√£o - dados n√£o foram salvos');
      return false;
    }
    
    const verificado = JSON.parse(verificacao);
    
    if (!Array.isArray(verificado) || verificado.length !== clientes.length) {
      console.error('[forceSaveClientes] ‚ùå Verifica√ß√£o falhou:', verificado.length, 'vs', clientes.length);
      return false;
    }
    
    console.log('[forceSaveClientes] ‚úÖ SALVAMENTO VERIFICADO COM SUCESSO');
    console.log('[forceSaveClientes] üìä Clientes salvos:', verificado.length);
    
    // Notificar IMEDIATAMENTE ap√≥s verifica√ß√£o bem-sucedida
    console.log('[forceSaveClientes] üîî Notificando listeners...');
    clientesStorageManager.notify();
    
    // Disparar evento global para sincroniza√ß√£o entre abas
    window.dispatchEvent(new CustomEvent('clienteAdicionado', {
      detail: { totalClientes: verificado.length }
    }));
    
    return true;
    
  } catch (error) {
    console.error('[forceSaveClientes] üí• Erro ao salvar:', error);
    return false;
  }
};

// Fun√ß√£o CORRIGIDA para adicionar cliente
export const addClienteToStorage = (novoCliente: Cliente): boolean => {
  try {
    console.log('[addClienteToStorage] üÜï INICIANDO ADI√á√ÉO DO CLIENTE:', novoCliente.email);
    console.log('[addClienteToStorage] üìã Dados completos do cliente:', novoCliente);
    
    // Carregar clientes atuais com fallback
    let clientesAtuais: Cliente[] = [];
    try {
      clientesAtuais = forceLoadClientes();
    } catch (error) {
      console.warn('[addClienteToStorage] ‚ö†Ô∏è Erro ao carregar clientes, iniciando com lista vazia');
      clientesAtuais = [];
    }
    
    console.log('[addClienteToStorage] üìä Clientes existentes antes da adi√ß√£o:', clientesAtuais.length);
    
    // Verificar duplicatas com seguran√ßa
    const emailNormalizado = novoCliente.email.toLowerCase().trim();
    const emailExiste = clientesAtuais.some(c => {
      try {
        return c.email && c.email.toLowerCase().trim() === emailNormalizado;
      } catch (error) {
        return false;
      }
    });
    
    if (emailExiste) {
      console.log('[addClienteToStorage] ‚ö†Ô∏è Email j√° existe:', emailNormalizado);
      return false;
    }
    
    // Criar cliente com dados completos
    const clienteCompleto: Cliente = {
      ...novoCliente,
      id: novoCliente.id || `cliente-form-${Date.now()}-${Math.random().toString(36).substring(2, 8)}`,
      createdAt: new Date(),
      updatedAt: new Date(),
    };
    
    console.log('[addClienteToStorage] üë§ Cliente preparado para salvamento:', {
      id: clienteCompleto.id,
      nome: clienteCompleto.nome,
      email: clienteCompleto.email,
      origem: clienteCompleto.origem
    });
    
    // Adicionar √† lista (FOR√áAR cria√ß√£o de nova array)
    const novosClientes = [...clientesAtuais, clienteCompleto];
    console.log('[addClienteToStorage] üìù Nova lista criada com', novosClientes.length, 'clientes');
    
    // Salvar DIRETAMENTE no localStorage para garantir persist√™ncia
    const dadosParaSalvar = JSON.stringify(novosClientes.map(c => ({
      ...c,
      createdAt: c.createdAt instanceof Date ? c.createdAt.toISOString() : c.createdAt,
      updatedAt: c.updatedAt instanceof Date ? c.updatedAt.toISOString() : c.updatedAt,
    })));
    
    console.log('[addClienteToStorage] üíæ Salvando DIRETAMENTE no localStorage...');
    localStorage.setItem(STORAGE_KEY, dadosParaSalvar);
    
    // Verifica√ß√£o tripla para garantir que foi salvo
    const verificacao1 = localStorage.getItem(STORAGE_KEY);
    if (!verificacao1) {
      console.error('[addClienteToStorage] ‚ùå PRIMEIRA VERIFICA√á√ÉO FALHOU');
      return false;
    }
    
    const clientesVerificacao = JSON.parse(verificacao1);
    if (!Array.isArray(clientesVerificacao) || clientesVerificacao.length !== novosClientes.length) {
      console.error('[addClienteToStorage] ‚ùå SEGUNDA VERIFICA√á√ÉO FALHOU:', clientesVerificacao.length, 'vs', novosClientes.length);
      return false;
    }
    
    // Verificar se o cliente espec√≠fico foi salvo
    const clienteSalvo = clientesVerificacao.find(c => c.email === novoCliente.email);
    if (!clienteSalvo) {
      console.error('[addClienteToStorage] ‚ùå TERCEIRA VERIFICA√á√ÉO FALHOU - Cliente n√£o encontrado na lista salva');
      return false;
    }
    
    console.log('[addClienteToStorage] ‚úÖ CLIENTE SALVO COM SUCESSO!');
    console.log('[addClienteToStorage] üìä Total de clientes agora:', clientesVerificacao.length);
    console.log('[addClienteToStorage] üéØ Cliente salvo:', {
      id: clienteSalvo.id,
      nome: clienteSalvo.nome,
      email: clienteSalvo.email,
      origem: clienteSalvo.origem
    });
    
    // Notificar IMEDIATAMENTE
    setTimeout(() => {
      console.log('[addClienteToStorage] üîî Notificando mudan√ßas...');
      clientesStorageManager.notify();
      
      // Disparar evento global
      window.dispatchEvent(new CustomEvent('clienteAdicionado', {
        detail: { 
          cliente: clienteCompleto,
          totalClientes: clientesVerificacao.length 
        }
      }));
    }, 100);
    
    return true;
    
  } catch (error) {
    console.error('[addClienteToStorage] üí• Erro geral:', error);
    return false;
  }
};

